using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using Microsoft.AspNetCore.Mvc.Rendering;
using ASPNET_Visual_Studio.Models;

namespace ASPNET_Visual_Studio.Pages.Movies
{
    public class CreateModel : PageModel
    {
        private readonly ASPNET_Visual_Studio.Models.ASPNET_Visual_StudioContext _context;

        public CreateModel(ASPNET_Visual_Studio.Models.ASPNET_Visual_StudioContext context)
        {
            _context = context;
        }

        public IActionResult OnGet()
        {
            return Page();
        }

        [BindProperty]
        public Movie Movie { get; set; }

        public async Task<IActionResult> OnPostAsync()
        {
            if (!ModelState.IsValid)
            {
                return Page();
            }

            _context.Movie.Add(Movie);
            await _context.SaveChangesAsync();

            return RedirectToPage("./Index");
        }
    }
}